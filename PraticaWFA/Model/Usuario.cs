using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace PraticaWFA.Model
{
    public class Usuario
    {
        public int id_cliente { get; set; } = 0;
        public string nome { get; set; } = null;
        public List<string> listaDeDescricoes { get; set; } = new List<string>();

        public string erroLogin { get; set; } = null;

        public Usuario()
        {

        }

        public Usuario(int id_cliente, string nome, List<string> listaDeDescricoes, string erroLogin)
        {
            this.id_cliente = id_cliente;
            this.nome = nome;
            this.listaDeDescricoes = listaDeDescricoes;
            this.erroLogin = erroLogin;
        }
    }
}
